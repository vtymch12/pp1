import math
x=3.7
y=4
sqrtX = math.sqrt(x)
print(f'Pierwiastek kwadratowy z {x} wynosi {sqrtX}')
powXY=math.pow(x,y)
print(f"X do potęgi y z {x} wynosi {powXY}")
sqrty=math.pow(x,1/y)
print (f"Pierwiastek y-tego stopnia z x wynosi: {sqrty}")
polekoła=math.pi*(y**2)
print(f"Pole koła o promieniu y wynosi: {polekoła}")
silna=math.factorial(y)
print(f"Silnię y wynosi:{silna}")
najwien=math.floor(x)
print(f"Największą możliwą liczbę całkowitą, mniejszą bądź równą x wynosi: {najwien}")
